// Generate random company name
function generateCompanyName(): string {
  const tech =
    companyTemplates.tech[
      Math.floor(Math.random() * companyTemplates.tech.length)
    ];
  const service =
    companyTemplates.services[
      Math.floor(Math.random() * companyTemplates.services.length)
    ];
  const industry =
    companyTemplates.industries[
      Math.floor(Math.random() * companyTemplates.industries.length)
    ];

  const patterns = [
    `${tech} ${service}`,
    `${tech} ${industry}`,
    `${service} ${industry}`,
    `${tech} ${service} ${industry}`,
    `${tech} India`,
    `${service} India`,
    `${tech} Global`,
    `${tech} Systems`,
  ];

  return patterns[Math.floor(Math.random() * patterns.length)];
}

